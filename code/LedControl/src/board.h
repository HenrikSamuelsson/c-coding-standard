#ifndef BOARD_H_
#define BOARD_H_

/** Port D, bit 15 output to red LED. */
#define PTD15    15

/** Port D, bit 16, output to green LED. */
#define PTD16    16

/** Port D, bit 0, output to blue LED. */
#define PTD0    0

/** Port C, bit 12, input from push button SW2. */
#define PTC12   12

#endif /* BOARD_H_ */
